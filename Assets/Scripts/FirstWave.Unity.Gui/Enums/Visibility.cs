namespace FirstWave.Unity.Gui.Enums
{
    public enum Visibility
    {
        /// <summary>
        /// Draw this control normally
        /// </summary>
        Visible,
        /// <summary>
        /// Do not draw the visual portion of this control, but reserve space for it
        /// </summary>
        Hidden,
        /// <summary>
        /// Do not draw this control and do not reserve space for it
        /// </summary>
        Collapsed
    }
}
